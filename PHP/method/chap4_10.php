<?php

//ビット演算子
//論理積
echo '7 & 5 = ' . (7 & 5) . "\n";
//論理和
echo '7 | 5 = ' . (7 | 5) . "\n";
//排他的論理和
echo '7 ^ 5 = ' . (7 ^ 5) . "\n";
//ビットを反転させる
echo '~-1 = ' . ~-1 . "\n";
//左シフト
echo ' 1 << 1 =' . (1 << 1) . "\n";
echo ' 1 << 2 =' . (1 << 2) . "\n";
echo ' 1 << 8 =' . (1 << 8) . "\n";
//右シフト
echo ' 1 >> 1 =' . (1 >> 1) . "\n";
echo ' 1 >> 2 =' . (1 >> 2) . "\n";
echo ' 1 >> 8 =' . (1 >> 8) . "\n";

?>
